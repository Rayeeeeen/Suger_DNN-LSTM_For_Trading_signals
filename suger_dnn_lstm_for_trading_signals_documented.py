def generate_signals_enhanced_RSI(df, k, vote_thresh, weighted,window_slope, RSI_thresh_up, RSI_thresh_down, slope_thresh,order):

    signals = pd.Series('HOLD', index=df.index, dtype='object')
    vote_series = pd.Series(np.nan, index=df.index)



    # Extract necessary arrays
    X = df.drop(columns=[ 'regime',	'is_local_min',	'is_local_max',	'Close'], errors='ignore').values
    prices = df['Close'].values
    regimes = df['regime'].values
    rsi = df['RSI'].values
    rsi_slope = df['RSI_slope'].values

    for i in range(k, len(df)):
        query = X[i]
        X_train = X[:i]
        idxs = lorentzian_knn_predict(X_train, query, k)

        if len(idxs) == 0:
            continue

        votes = []
        dist_list = []

        for j in idxs:
            if j < window_slope or j >= len(rsi):
                continue

            r = rsi[j]
            s = rsi_slope[j]

            if r < RSI_thresh_down and s > slope_thresh:
                votes.append(1)
            elif r > RSI_thresh_up and s < -slope_thresh:
                votes.append(-1)
            else:
                votes.append(0)

            dist = np.linalg.norm(X_train[j] - query)
            dist_list.append(dist)

        if len(votes) == 0:
            continue

        if weighted:
            epsilon = 1e-5
            dist_array = np.array(dist_list)
            weights = 1 / (dist_array + epsilon)
            weights /= weights.sum() + 1e-10
            vote_score = np.dot(votes, weights)
        else:
            vote_score = sum(votes) / (len(votes) + 1e-10)

        vote_score = round(vote_score, 2)
        vote_series.iloc[i] = vote_score

        # Apply final signal filtering with local extrema condition

        if vote_score >= vote_thresh and df['is_local_min'].iloc[i]:
                signals.iloc[i] = 'BUY'
        elif vote_score <= -vote_thresh and df['is_local_max'].iloc[i]:
                signals.iloc[i] = 'SELL'


    return signals

