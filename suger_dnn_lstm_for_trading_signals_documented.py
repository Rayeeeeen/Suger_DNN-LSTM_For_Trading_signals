def lorentzian_distance(a, b):
    a = np.array(a, dtype=float)
    b = np.array(b, dtype=float)
    return np.sum(np.log1p(np.abs(a - b)))



# === KNN Classification with Lorentzian Distance ===
def lorentzian_knn_predict(X, query_vector, k):
    distances = np.array([lorentzian_distance(row, query_vector) for row in X])
    knn_idx = distances.argsort()[:k]
    return knn_idx

