def decode_with_threshold(proba_vec, tau):
    """
    Decodes a probability vector into a trading signal using a confidence threshold.

    Args:
        proba_vec (array-like): [p_hold, p_buy, p_sell]
        tau (float): threshold value (e.g., 0.6)

    Returns:
        int: 0 for HOLD, 1 for BUY, 2 for SELL
    """
    p_hold, p_buy, p_sell = proba_vec

    if p_sell >= tau and p_buy >= tau:
        return 0  # SELL and p_sell == max(proba_vec)

    elif p_buy >= tau :
        return 1  # BUY and p_buy == max(proba_vec)

    elif p_sell >= tau :
        return 2  # SELL and p_sell == max(proba_vec)


    elif p_hold >= tau :
        return 0  # HOLD and p_hold == max(proba_vec)
    else:
        return 0 # fallback: take the highest prob regardless of tau

def build_model_LSTM(input_shape,lstm_size,lstm_size_2,dense_size, gamma, alpha):
    model = keras.models.Sequential()
    model.add(keras.layers.LSTM(lstm_size, return_sequences=True, input_shape=(input_shape)))  # Example input
    model.add(keras.layers.LSTM(lstm_size_2))
    model.add(keras.layers.PReLU(shared_axes=[1]))  # Apply PReLU to the output of LSTM
    model.add(keras.layers.Dropout(0.2))
    model.add(keras.layers.LayerNormalization())
    model.add(keras.layers.Dense(dense_size))
    model.add(keras.layers.PReLU())
    model.add(keras.layers.Dense(3, activation='softmax'))

    model.compile(
    optimizer='adamW',
    loss=focal_loss(gamma=gamma, alpha=alpha),  # alpha = list of class weights /focal_loss(gamma=0.5, alpha=alpha)
    metrics = ['accuracy']+ [
    tf.keras.metrics.Precision(class_id=1, name='prec_buy'),
    tf.keras.metrics.Recall   (class_id=1, name='rec_buy'),
    tf.keras.metrics.Precision(class_id=2, name='prec_sell'),
    tf.keras.metrics.Recall   (class_id=2, name='rec_sell')
]
)
    return model

