def compute_buy_and_hold(df, price_col='Close', initial_cash=1000):
    start_price = df[price_col].iloc[0]
    return df[price_col] / start_price * initial_cash

def filter_signals(series, trad_num):
    series = series.copy()
    last_trade = 'HOLD'
    trade_count = 0

    for i in range(len(series)):
        current = series[i]

        if current == 'HOLD':
            continue

        if current == last_trade:
            trade_count += 1
            if trade_count > trad_num:
                series[i] = 'HOLD'
        else:
            # Signal changed direction → reset
            last_trade = current
            trade_count = 1

    return series

def simulate_strategy_with_borrow_and_evolution(df,signal_col='signal', price_col='Close', initial_investment=100000):
    df = df.copy()
    df[signal_col] = df[signal_col].astype(str).str.strip().str.upper()

    cash = initial_investment
    portfolio = 0
    count_long = 0
    count_short = 0
    borrow = 0
    position = None
    entry_price = None

    results = []

    for i in range(len(df)):
        date = df.index[i]
        price = df.iloc[i][price_col]
        signal = df.iloc[i][signal_col]

        # Update the portfolio to reflect market movement before acting on new signal
        if position == 'LONG':
            portfolio = portfolio * (price / entry_price)
            entry_price = price  # reset entry to current for relative growth
        elif position == 'SHORT':
            portfolio = portfolio * (price / entry_price)
            entry_price = price

        # === Handle BUY signal ===
        if signal == 'BUY':
            if position == 'SHORT':
                # Close short: portfolio is negative
                cash += portfolio
                portfolio = 0
                position = None
                entry_price = None

            invest_amount = initial_investment


            if count_long == 0:
                final_coef = 1  # full exposure
            else:
                n = 2 + count_long  # prevents log(1)
                final_coef = np.log(n / (n - 1)) *0.5



            adjusted_investment = invest_amount * final_coef

            if cash < adjusted_investment:
                needed = adjusted_investment - cash
                borrow += needed
                cash += needed

            cash -= adjusted_investment
            portfolio += adjusted_investment
            position = 'LONG'
            count_long += 1
            count_short = 0
            entry_price = price

        # === Handle SELL signal ===
        elif signal == 'SELL':
            if position == 'LONG':
                # Close long
                cash += portfolio
                portfolio = 0
                position = None
                entry_price = None

                # Repay borrow
                if borrow > 0:
                    repayment = min(cash, borrow)
                    cash -= repayment
                    borrow -= repayment

            invest_amount = initial_investment


            if count_short == 0:
                final_coef = 1  # full exposure
            else:
                n = 2 + count_short  # prevents log(1)
                final_coef = np.log(n / (n - 1))*0.5



            adjusted_investment = invest_amount * final_coef

            cash += adjusted_investment
            portfolio -= adjusted_investment
            position = 'SHORT'
            count_short +=1
            count_long = 0
            entry_price = price

        total_equity = cash + portfolio - borrow

        results.append({
            'date': date,
            'signal': signal,
            'cash': round(cash, 2),
            'portfolio_value': round(portfolio, 2),
            'borrow': round(borrow, 2),
            'total_equity': round(total_equity, 2),
        })

    return pd.DataFrame(results).set_index('date')

