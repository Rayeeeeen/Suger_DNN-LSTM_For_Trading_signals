def focal_loss(gamma, alpha):
    """
    Focal loss for multi-class classification
    :param gamma: focusing parameter
    :param alpha: weight for each class (list or numpy array of shape [num_classes])
    :return: focal loss function
    """
    def focal_loss_fixed(y_true, y_pred):
        # Clip the prediction values to avoid log(0) error
        epsilon = K.epsilon()
        y_pred = K.clip(y_pred, epsilon, 1. - epsilon)

        # Calculate cross entropy
        cross_entropy = -y_true * K.log(y_pred)

        # Calculate focal loss
        if alpha is not None:
            alpha_tensor = tf.convert_to_tensor(alpha, dtype=tf.float32)
            cross_entropy = alpha_tensor * cross_entropy

        loss = K.pow(1 - y_pred, gamma) * cross_entropy

        return K.sum(loss, axis=1)  # sum over classes

    return focal_loss_fixed

