def rolling_standard_scaling(df, features, window):
    """
    Applies rolling z-score normalization (mean-std) over a specified window.

    Args:
        df (pd.DataFrame): Feature dataframe
        y (array-like): Corresponding labels (target values)
        features (list): List of column names to scale
        window (int): Rolling window size

    Returns:
        X (pd.DataFrame): Scaled features with original index (after dropna)
        y (np.array): Aligned labels
    """
    df = df.copy()
      # temporarily store target

    for col in features:
        df[col] = (df[col] - df[col].rolling(window=window, min_periods=window).mean()) / \
                  df[col].rolling(window=window, min_periods=window).std()

    df.dropna(inplace=True)

    X = df[features]
    y =df['signal']
    return X, y

